public class Sun {

    double diameter;
    String col;
    String name;

    public Sun(double dm, String c, String n) //This is by far the simplist of the object classes, it simply contains the diameter, colour and name of the sun. Nothing else is needed as the sun is static and unmoving at the centre
    {
        diameter = dm;
        col = c;
        name = n;
    }
}
